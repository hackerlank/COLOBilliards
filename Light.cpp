void Light::SetLight(void)
{
	//ライトの初期化
	D3DXVECTOR3 vecDir; //計算用変数
	LPDIRECT3DDEVICE9 pDevice=Manager::GetRenderer()->GetDevice();


	ZeroMemory(&m_aLight[0],sizeof(D3DLIGHT9)); //メモリの中身をすべてクリア
	m_aLight[0].Type = D3DLIGHT_DIRECTIONAL;	//平行光源
	m_aLight[0].Diffuse = D3DXCOLOR(1.0f,0.9f,0.9f,1.0f); //光の色
	vecDir = D3DXVECTOR3(0.5f,-0.7f,0.2f); //(-1<= n <=1)
	D3DXVec3Normalize((D3DXVECTOR3*)&m_aLight[0].Direction,&vecDir);

	pDevice->SetLight(0,&m_aLight[0]);	//ライトの設定
	pDevice->LightEnable(0,TRUE);		//ライトを有効にする
	

	ZeroMemory(&m_aLight[1],sizeof(D3DLIGHT9)); //メモリの中身をすべてクリア
	m_aLight[1].Type = D3DLIGHT_DIRECTIONAL;	//平行光源
	m_aLight[1].Diffuse = D3DXCOLOR(1.0f,0.9f,0.9f,0.5f); //光の色
	vecDir = D3DXVECTOR3(0.5f,0.1f,-0.8f); //(-1<= n <=1)
	D3DXVec3Normalize((D3DXVECTOR3*)&m_aLight[1].Direction,&vecDir);

	pDevice->SetLight(1,&m_aLight[1]);	//ライトの設定
	pDevice->LightEnable(1,TRUE);		//ライトを有効にする
	

	ZeroMemory(&m_aLight[2],sizeof(D3DLIGHT9)); //メモリの中身をすべてクリア
	m_aLight[2].Type = D3DLIGHT_DIRECTIONAL;	//平行光源
	m_aLight[2].Diffuse = D3DXCOLOR(1.0f,0.9f,0.9f,0.5f); //光の色
	vecDir = D3DXVECTOR3(0.1f,-0.8f,2.1f); //(-1<= n <=1)
	D3DXVec3Normalize((D3DXVECTOR3*)&m_aLight[2].Direction,&vecDir);

	pDevice->SetLight(2,&m_aLight[2]);	//ライトの設定
	pDevice->LightEnable(2,TRUE);		//ライトを有効にする
}